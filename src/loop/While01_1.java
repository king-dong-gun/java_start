package loop;

public class While01_1 {
    public static void main(String[] args) {
        int count = 0;
        count = count + 1;
        System.out.println("현재숫자:" + count);
        count = count + 1;
        System.out.println("현재숫자:" + count);
        count = count + 1;
        System.out.println("현재숫자:" + count);          //100번 반복하려면 100개 작성해야하기 때문에 반복문 사용
    }
}
