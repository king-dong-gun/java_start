package method.ex;

public class MethodEx01 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;

        int sum = a + b + c;
        double average = (double) sum / 3;
        System.out.println("평균값: " + average);

        int x = 15;
        int y = 25;
        int z = 35;

        sum = x + y + z;
        average = (double) sum / 3;
        System.out.println("평균값: " + average);
    }
}
