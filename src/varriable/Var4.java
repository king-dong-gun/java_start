package varriable;

public class Var4 {
    public static void main(String[] args) {
        int a;  //변수 선언
        a = 1;  //변수 초기화
        System.out.println(a);
        int b = 10;  //변수 선언, 초기화 동시에
        System.out.println(b);
        int c = 20, d = 2; // 여러개 변수 초기화 동시에
        System.out.println(c + d);
    }
}
